dat = read.csv('Deeper.csv', stringsAsFactors=T, head=T)
dat0 = dat[which(dat$VIEW == 0), ] #no of 0s i.e. no outcome values in y
dat1 = dat[which(dat$VIEW == 1), ]#no of 1s i.e. yes outcome values in y
set.seed(1)

ind = sample(1:nrow(dat1), nrow(dat0)) #taking sample by referring total no of 1s
dat00 = dat1[ind, ]#seperating the equal no of 0s with 1s
datnew = rbind.data.frame(dat00, dat0)#combining separated 0s and original no of 1s
write.csv(datnew, file='datanew.csv', row.names=F)

data = read.csv('datanew.csv')
